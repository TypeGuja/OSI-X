//! Общий интерфейс [`MotorDriver`], реализуемый всеми драйверами двигателей
//! осей станка.
//!
//! Прошивка (планировщик движения, генератор шагов, обработчик G-Code)
//! работает исключительно через этот трейт и никогда не знает, что именно
//! приводит ось в движение — TMC2209 в режиме STEP/DIR, ULN2003 с
//! программным управлением фазами, или в будущем A4988/DRV8825/TMC5160.
//! Это единственная граница абстракции, необходимая для замены двигателя
//! оси Z на TMC2209 без изменения остального кода (см. ТЗ).

use crate::error::AppResult;
use crate::types::MotorDirection;

/// Единый интерфейс драйвера двигателя одной оси.
///
/// # Контракт реализации
///
/// - [`MotorDriver::step`] должен выполнять ровно один шаг (или один такт
///   фазовой последовательности для драйверов без физического STEP-входа,
///   например [`crate::drivers::motor::uln2003::Uln2003Driver`]) в
///   направлении, установленном последним вызовом [`MotorDriver::set_direction`].
/// - [`MotorDriver::set_speed`] задаёт информационную/диагностическую
///   целевую скорость (шагов в секунду). Фактическая частота вызовов
///   [`MotorDriver::step`] определяется генератором шагов
///   (`motion::step_generator`) на основе профиля разгона/торможения —
///   драйвер не обязан и не должен сам генерировать тайминг.
/// - [`MotorDriver::stop`] обязана быть безопасной для вызова в любом
///   состоянии (в том числе из обработчика аварийной остановки) и должна
///   немедленно прекращать подачу энергии на обмотки/STEP-линию.
/// - Реализации не должны паниковать; любая аппаратная ошибка (обрыв UART,
///   таймаут, некорректный ответ драйвера) возвращается как
///   [`crate::error::AppError::MotorDriver`].
pub trait MotorDriver {
    /// Включает силовые каскады драйвера (отпускает удерживающий тормоз тока
    /// покоя, если применимо, и переводит выходы в рабочий режим).
    fn enable(&mut self) -> AppResult<()>;

    /// Выключает силовые каскады драйвера (обмотки обесточиваются или
    /// переводятся в ток покоя `0`, в зависимости от драйвера).
    fn disable(&mut self) -> AppResult<()>;

    /// Возвращает `true`, если драйвер в данный момент включён.
    fn is_enabled(&self) -> bool;

    /// Устанавливает логическое направление вращения для последующих
    /// вызовов [`MotorDriver::step`].
    fn set_direction(&mut self, direction: MotorDirection) -> AppResult<()>;

    /// Выполняет один шаг в текущем установленном направлении.
    fn step(&mut self) -> AppResult<()>;

    /// Сообщает драйверу целевую скорость движения, шагов в секунду.
    ///
    /// Не управляет фактическим таймингом вызовов [`MotorDriver::step`] —
    /// используется драйверами, которым скорость нужна для собственных
    /// решений (например, для будущего динамического снижения тока на
    /// высоких скоростях).
    fn set_speed(&mut self, steps_per_second: f32) -> AppResult<()>;

    /// Немедленно останавливает движение и снимает энергию с обмоток.
    ///
    /// Должна быть безопасна для вызова из обработчика аварийной остановки
    /// независимо от текущего состояния драйвера.
    fn stop(&mut self) -> AppResult<()>;
}
